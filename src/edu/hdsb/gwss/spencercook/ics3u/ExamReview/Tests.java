    public void importData() {
        if (file.exists()) {
            Builder builder = new Builder();
            try {
                doc = builder.build(file);
                rootElement = doc.getRootElement();
                //children = rootElement.getChildElements();
                
                
            } catch (Exception e) {
                System.err.print(e);
            }
        } else {
            rootElement = new Element("people");
            doc = new Document(rootElement);
        }
    }

