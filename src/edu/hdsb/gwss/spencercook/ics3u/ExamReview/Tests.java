    public void exportData() {
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(doc.toXML());
            bw.close();
        } catch (Exception e) {
            System.err.println(e);
        }
    }
    
